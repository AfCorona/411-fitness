import Foundation

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// Meal Category
/// Enum to store meal type
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

enum MealCategory: String, CaseIterable, Identifiable {
    case breakfast = "Breakfast"
    case lunch = "Lunch"
    case dinner = "Dinner"
    case snack = "Snack"
    
    var id: String { self.rawValue }
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// Meal Model
/// All the values needed for a meal
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct Meal: Identifiable {
    let id = UUID()
    var name: String
    var category: MealCategory
    
    var protein: Int  // grams
    var carbs: Int    // grams
    var fat: Int      // grams
    var calories: Int // kcal
}

